$(document).ready(function () {

    var language = localStorage.getItem('ahlLanguage');
    switch (language) {
        case "Türkçe": LJS_Language = "tr"; break;
        case "English": LJS_Language = "en"; break;
        case "Español": LJS_Language = "es"; break;
        case "L'Italiano": LJS_Language = "it"; break;
        case "Deutsch": LJS_Language = "de"; break;
    }
    LanguageChange(LJS_Language)
});


function LanguageChange(lg) {
    LJS_Language = lg;
    let country = "turkiye";
    let language = "Türkçe";
    switch (lg) {
        case "tr": country = "turkiye"; language = "Türkçe"; break;
        case "en": country = "england"; language = "English"; break;
        case "es": country = "spain"; language = "Español"; break;
        case "it": country = "italy"; language = "L'Italiano"; break;
        case "de": country = "germany"; language = "Deutsch"; break;
    }
    localStorage.setItem('ahlLanguage', language);
    let imgSrc = "/assets/images/flags/" + country + ".jpg";
    document.getElementById("selectedFlag").src = imgSrc;
    document.getElementById("selectedLanguage").innerHTML = language;
    LoadPage();
    IndexLoadPage();
    RequestCreateLoadPage();
}

function getTitle(obj) {
    return Object.keys(obj).filter(key => obj[key] === LJS_Language);
}
